#Programa que hace Operaciones Aritmeticas basicas
a=10
b=5

#suma 
suma=a+b
print(f"{a}+{b}= ",{suma})

#resta
resta=a-b
print(f"{a}-{b} = ",{resta})

#multiplicacion
multiplicacion=a*b
print(f"{a}*{b} =",{multiplicacion})

#division
division=a/b
print(f"{a}/{b} =", {division})

